                  <h2 class="u-text u-text-1"> Архитектура России 18 века </h2>
                  <p class="u-text u-text-2">
                    <br> 18 век в архитектуре и градостроительстве России считается важным и знаковым. 
                    Он характеризуется тремя направлениями – барокко, рококо и классицизмом, 
                    проявившимися последовательно в течение века. В этот период появляются новее города, 
                    создаются объекты, которые в наше время считаются признанными историческими и архитектурными памятниками.
                  </p>
                    <p>
                    <h2>
                    Первая треть 18 века. Барокко
                    </h2>
                  </p>
                  <p>
                  В первой трети столетия все архитектурные преобразования неразрывно связаны с именем Петра Первого. 
                  За этот период российские города претерпели значительные изменения и в социально-экономическом плане и в архитектурно-планировочном. 
                  Именно в это время развивается промышленность, 
                  что приводит к строительству множества промышленных городов и поселков. 
                  Политическая обстановка в стране и за ее пределами создала предпосылки к тому, 
                  что главенствующие в этот период дворянство и купечество оказываются втянутыми в строительство объектов общественного назначения. 
                  Если до этого периода самыми величественными и красивыми создавались в основном церкви и царские резиденции (палаты), 
                  то в начале 18 века в городах уделяется большое значение внешнему виду обычных жилых зданий, 
                  а также появляющихся театров, набережных, идет массовое строительство ратушей, школ, больниц (так называемых гошпиталей), домов для сирот. 
                  С 1710 года в строительстве активно начинают использовать кирпич вместо деревянных построек. 
                  Правда, первоначально это нововведение касалось, прежде всего, столиц, 
                  тогда как для периферии камень и кирпич надолго еще оставались под запретом.
                  </p>
                  <p>
                  Петром I создается особая комиссия, которая в будущем станет главным органом государственного проектирования как столицы, 
                  так и других городов. Гражданское строительство уже преобладает над церковным. 
                  Большое значение придается не только фасадам, но и облику всего города – дома начинают строить фасадами вдоль улиц, 
                  производится разуплотнение застройки в противопожарных целях, благоустраиваются улицы, 
                  мостятся дороги, решается вопрос с уличным освещением, по обочинам высаживаются деревья. 
                  Во всем этом чувствуется зримое влияние запада и твердая рука Петра, 
                  своими указами совершившего в те годы практически революцию в градостроительстве. 
                  Поэтому не удивительно, что России за короткий срок удается практически догнать Европу, 
                  выйдя на достойный уровень по уровню градостроительства и благоустройства городов.
                  </p>
                  <p>
                    <figure class="text">
                  <img src="..\images/Лефортово.png" alt="Лефортово"><br>
