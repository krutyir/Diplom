                </p>
                  <p>
                  Главным архитектурным событием начала века считается строительство Санкт-Петербурга. 
                  Именно с этого города и московской Лефортовской слободы начинаются серьезные преобразования в архитектурном облике прочих городов. 
                  Ориентированный на запад Петр Первый приглашает иностранных архитекторов и отправляет российских специалистов на обучение в Европу.
                  </p>
                  <p>
                  В Россию приезжают Трезини, Леблон, Мичетти, Шедель, Растрелли (отец) и другие именитые архитекторы, 
                  которым суждено внести большой вклад в русское зодчество первой четверти 18 века. 
                  Что интересно, если в начале своего творческого пути в России они четко следовали своим принципам и западному архитектурному мышлению, 
                  то уже через какой-то период времени специалисты-историки отмечают влияние нашей культуры и самобытности, 
                  которое прослеживается в их более поздних работах.
                  </p>                  
                  <p>
                  В первой трети 18 века преимущественным направлением в архитектуре и строительстве является барокко. 
                  Этому направлению свойственно сочетание реальности и иллюзии, пышности и контраста. 
                  Строительство Санкт-Петербурга начинается с заложения Петропавловской крепости в 1703 году и Адмиралтейства в 1704 году. 
                  Петр поставил серьезные задачи перед архитекторскими кадрами того периода в плане 
                  соответствия нового города передовым европейским принципам градостроительства. 
                  Благодаря слаженной работе русских архитекторов и их иностранных коллег, 
                  северная столица приобрела формально западные черты в слиянии с традиционно русскими. 
                  Тот стиль, в котором создавались многочисленные помпезные дворцы, церкви, государственные учреждения, 
                  музеи и театры сейчас часто называют русским барокко или барокко Петровской эпохи.
                  </p>
                  <p>
                    <figure class="text">
                  <img src="..\images/Петро.png" alt="Петро"><br>
                  <figcaption>
                  Петро
                  </figcaption>  
                  </figure>
                </p>
                  <p>
                  В этот период создаются Петропавловский собор, летний дворец Петра Первого, Кунсткамера, 
                  дворец Меньшиикова, здание Двеннадцати коллегий в Санкт-Петербурге. 
                  В стиле барокко оформлены, созданные в этот и более поздний период, ансамбли Зимнего дворца, 
                  Царского села, Петергофа, Смольного монастыря, дворца Строгановых. 
                  В Москве — это церкви Архангела Гавриила и Иоанна Воина на Якиманке, 
                  характерными элементами свойственными этому периоду оформлен главный въезд в Арсенальный двор Кремля. 
                  Среди важных объектов провинциальных городов стоит отметить Петропавловский собор в Казани.
                  <h2>
                  Середина 18 века. Барокко и рококо
                  </h2>
                  <p>
                  Несмотря на то, что смерть Петра I была большой утратой для государства, 
                  она уже не оказала существенного влияния на развитие градостроительства и архитектуры того периода. 
                  Русские архитекторы, работающие в Петербурге под началом иностранцев, перенимали их опыт, 
                  вернулись на родину и те, кто был отправлен на обучение за границу. 
                  Страна на тот момент обладала сильными кадрами. 
                  Ведущими русскими архитекторами того периода являлись Еропкин, 
                  Усов, Коробов, Земцов, Мичурин, Бланк и другие.
                  </p>
                  <p>
                  Стиль характерный этому периоду называется рококо и является сочетанием барокко и зарождающегося классицизма. 
                  В нем проявляется галантность, уверенность. 
                  Рококо больше характерен для интерьерных решений того времени. 
                  В строительстве зданий еще отмечается пышность и помпезность барокко, 
                  а также начинают проявляться строгие и простые черты классицизма.
                  </p>
                  <p>
                  Этот период, совпавший с правлением дочери Петра – Елизаветы, отмечен творчеством Растрелли–сына. 
                  Воспитанный на русской культуре, в своих работах он демонстрировал не только блеск и роскошь дворцовой архитектуры, 
                  но и понимание русского характера, русской натуры. 
                  Его проекты вместе с творчеством современников Квасова, Чевакинского, Ухтомского органично вписались в историю русского зодчества 18 века. 
                  С легкой руки Растрелли купольные композиции стали появляться не только в столице, 
                  но и в других российских городах, постепенно заменив собой шпилеобразные. 
                  Парадность и размах его дворцовых ансамблей не знает аналогов в русской истории. 
                  Но при всем признании и роскоши искусство Растрелли и его современников продержалось не долго, 
                  и на смену ему во второй половине 18 века приходит волна классицизма. 
                  В этот период создаются самые масштабные проекты — новый генеральный план Петербурга и проект перепланировки Москвы.
                  </p>
                  <h2>
                  Конец 18 века. Классицизм
                  </h2>
                  <p>
                  <figure class="text">
                  <img src="..\images/Мраморный-дворец.png" alt="Мраморный дворец"><br>
                  <figcaption>
                  Мраморный дворец
                  </figcaption>  
                  </figure>
                  </p>
                  <p>
                  В русской архитектуре в последнюю треть 18 века начинают проявляться черты нового направления, 
                  которое впоследствии назвали русским классицизмом. 
                  К концу века классицизм прочно закрепляется как основное направление искусства и архитектуры. 
                  Этому течению характерны строгость античных форм, простота и рациональность конструкций. 
                  В отличие от строений в стиле барокко, заполнивших собой Петербург и его окрестности, 
                  классицизм наиболее проявил себя в московских постройках того времени. 
                  Среди многих, стоит отметить дом Пашкова, здание Сената, Царицынский комплекс, 
                  дом Голицына, дворец Разумовских, которые считаются наиболее яркими примерами проявления классицизма в архитектуре. 
                  В Петербурге в это время возводились Таврический дворец, Александро-Невская лавра, 
                  Мраморный дворец, Эрмитаж, Эрмитажный театр, Академия наук. 
                  Выдающимися архитекторами того времени по праву считают Казакова, Баженова, Ухтомского и многих других.
                  </p>
                  <p>
                  На период 18 века относятся и изменения, коснувшиеся множества провинциальных городов того времени, – 
                  Ярославля, Костромы, Нижнего Новгорода, Архангельска, Одоева Богородицка, Ораниенбаума, ныне Ломоносова, Царского села, ныне Пушкина и так далее. 
                  С 18 века берут свое начало Петрозаводск, Таганрог, Екатеринбург и многие другие города, 
                  ставшие в тот период и впоследствии важными промышленными и экономическими центрами российского государства.
                  </p>
